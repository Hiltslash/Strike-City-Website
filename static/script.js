function dispACC() {
    if (document.getElementById("dispACCINFO").style.display == "none") {
        document.getElementById("dispACCINFO").style.display = "block"
        return
    }
    document.getElementById("dispACCINFO").style.display = "none"
}
function dispAdmin() {
    if (document.getElementById("dispAdmin").style.display == "none") {
        document.getElementById("dispAdmin").style.display = "block"
        return
    }
    document.getElementById("dispAdmin").style.display = "none"
}